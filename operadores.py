# > Operações Matemáticas (Aritméticas)

"""
    - Soma: +
    - Subtração: - 
    - Multiplicação: *
    - Divisão: /
    - Divisão Inteira: // 
    - Resto da Divisão: %
    - Potenciação: **
"""

numero1: int = 10
numero2: int = 20

print(numero2, '+' ,numero1, "=", numero2 + numero1)
print(numero2, '-' ,numero1, "=", numero2 - numero1)
print(numero2, '*' ,numero1, "=", numero2 * numero1)
print(numero2, '/' ,numero1, "=", numero2 / numero1)
print(numero2, '//' ,numero1, "=", numero2 // numero1)
print(numero2, '%' ,numero1, "=", numero2 % numero1)
print(numero2, '**' ,numero1, "=", numero2 ** numero1)

# Operadores Booleanos

idade1: int = 10
idade2: int = 20
altura1: float = 1.78
